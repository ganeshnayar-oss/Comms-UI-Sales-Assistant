export const LANGUAGE_PACKS = {
  "en-US": {},
  "fr-FR": fr,
  "es-ES": es,
};

export function getSupportedLocale(locale) {
  return LANGUAGE_PACKS[locale] ? locale : "en-US";
}

export function translateStaticText(text, locale) {
  const source = String(text || "");
  const trimmed = source.replace(/\s+/g, " ").trim();
  const pack = LANGUAGE_PACKS[getSupportedLocale(locale)] || {};

  if (!trimmed || getSupportedLocale(locale) === "en-US") {
    return source;
  }

  if (pack[trimmed]) {
    return source.replace(trimmed, pack[trimmed]);
  }

  const localizeTerm = (value) => pack[value] || value;

  const closestCatalogMatch = trimmed.match(/^Closest catalog match in the (.+) category\.$/);
  if (closestCatalogMatch) {
    const category = localizeTerm(closestCatalogMatch[1]);
    const translated =
      locale === "fr-FR"
        ? `Meilleure correspondance du catalogue dans la categorie ${category}.`
        : `Coincidencia mas cercana del catalogo en la categoria ${category}.`;
    return source.replace(trimmed, translated);
  }

  const closestPromotionMatch = trimmed.match(/^Closest bundled promotion match in the (.+) category\.$/);
  if (closestPromotionMatch) {
    const category = localizeTerm(closestPromotionMatch[1]);
    const translated =
      locale === "fr-FR"
        ? `Meilleure correspondance de promotion groupee dans la categorie ${category}.`
        : `Coincidencia mas cercana de promocion empaquetada en la categoria ${category}.`;
    return source.replace(trimmed, translated);
  }

  const bestMatch = trimmed.match(/^Best match for (.+) needs in the (.+) category( bundled promotion)?\.$/);
  if (bestMatch) {
    const signals = bestMatch[1]
      .split(", ")
      .map((signal) => localizeTerm(signal))
      .join(", ");
    const category = localizeTerm(bestMatch[2]);
    const promotionSuffix = bestMatch[3]
      ? locale === "fr-FR"
        ? " avec promotion groupee"
        : " con promocion empaquetada"
      : "";
    const translated =
      locale === "fr-FR"
        ? `Meilleure correspondance pour les besoins ${signals} dans la categorie ${category}${promotionSuffix}.`
        : `Mejor coincidencia para necesidades de ${signals} en la categoria ${category}${promotionSuffix}.`;
    return source.replace(trimmed, translated);
  }

  if (trimmed.startsWith("Recommended offer: ")) {
    return source.replace("Recommended offer:", locale === "fr-FR" ? "Offre recommandee :" : "Oferta recomendada:");
  }

  const resultsMatch = trimmed.match(/^(\d+) Results$/);
  if (resultsMatch) {
    const translated = locale === "fr-FR" ? `${resultsMatch[1]} resultats` : `${resultsMatch[1]} resultados`;
    return source.replace(trimmed, translated);
  }

  const productsAddedMatch = trimmed.match(/^(\d+) products? added$/);
  if (productsAddedMatch) {
    const translated =
      locale === "fr-FR"
        ? `${productsAddedMatch[1]} produit${productsAddedMatch[1] === "1" ? "" : "s"} ajoute${productsAddedMatch[1] === "1" ? "" : "s"}`
        : `${productsAddedMatch[1]} producto${productsAddedMatch[1] === "1" ? "" : "s"} agregado${productsAddedMatch[1] === "1" ? "" : "s"}`;
    return source.replace(trimmed, translated);
  }

  if (trimmed.startsWith("Siebel order ") && trimmed.endsWith(" is active.")) {
    const id = trimmed.replace(/^Siebel order /, "").replace(/ is active\.$/, "");
    return locale === "fr-FR" ? `La commande Siebel ${id} est active.` : `El pedido de Siebel ${id} esta activo.`;
  }

  return source;
}

export function getEnglishSourceFromTranslation(text, locale) {
  const value = String(text || "").replace(/\s+/g, " ").trim();
  const pack = LANGUAGE_PACKS[getSupportedLocale(locale)] || {};
  const match = Object.entries(pack).find(([, translated]) => translated === value);
  return match?.[0] || value;
}
